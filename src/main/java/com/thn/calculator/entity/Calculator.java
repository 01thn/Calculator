package com.thn.calculator.entity;

public class Calculator {
    public Calculator() {
    }

    public double sum(double var1, double var2) {
        return var1 + var2;
    }

    public double minus(double var1, double var2) {
        return var1 - var2;
    }

    public double multiply(double var1, double var2) {
        return var1 * var2;
    }

    public double divide(double var1, double var2) {
        return var1 / var2;
    }
}
