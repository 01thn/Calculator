package com.thn.calculator.service;

public class CalculatorService {
    public static double sum(double var1, double var2) {
        return var1 + var2;
    }

    public static double minus(double var1, double var2) {
        return var1 - var2;
    }

    public static double multiply(double var1, double var2) {
        return var1 * var2;
    }

    public static double divide(double var1, double var2) {
        return var1 / var2;
    }
}
